package Operators;

public class Assignment {
    public static void main(String[] args) {
        int c = 10;

        // Assign value
        c = 5; // c is now 5

        // Add and assign
        c += 3; // c is now 8

        // Subtract and assign
        c -= 2; // c is now 6

        // Multiply and assign
        c *= 4; // c is now 24

        // Divide and assign
        c /= 3; // c is now 8

        // Modulus and assign
        c %= 2; // c is now 0
    }
}
