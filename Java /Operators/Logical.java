package Operators;

public class Logical {
    public static void main(String[] args) {
        boolean p = true;
        boolean q = false;

        // Logical AND
        boolean andResult = (p && q); // andResult is now false

        // Logical OR
        boolean orResult = (p || q); // orResult is now true

        // Logical NOT
        boolean notResult = !p; // notResult is now false
    }
}
