import java.lang.*;
public class Printing {
    public static void main(String[] args) {
        System.out.println("Hello");
        // System is a final class in the java.lang package. It cannot be instantiated,
        // and it provides a collection of static fields and methods that offer
        // system-related functionalities like standard input, output, error streams, environment
        // variables etc.
        System.out.printf("%d is the number",15);
    }
}
